/**
 * @file model_builder.h
 * @brief Интерфейс построения биграммной марковской модели.
 *
 * Предоставляет функцию build_model() и коды ошибок.
 * Все идентификаторы используют snake_case, суффиксы _s (структуры), _e (перечисления).
 */

#ifndef MODEL_BUILDER_H
#define MODEL_BUILDER_H

/**
 * @brief Коды ошибок построения модели.
 */
typedef enum {
    BUILD_OK                  = 0,  /**< Успешное завершение. */
    BUILD_ERR_OPEN_INPUT_DIR  = 1,  /**< Не удалось открыть входную директорию. */
    BUILD_ERR_EMPTY_CORPUS    = 2,  /**< Корпус не содержит ни одного слова. */
    BUILD_ERR_ALLOC           = 3,  /**< Ошибка выделения памяти. */
    BUILD_ERR_OUTPUT_FILE     = 4,  /**< Ошибка записи в выходные файлы. */
    BUILD_ERR_MKDIR           = 5,  /**< Не удалось создать выходную директорию. */
    BUILD_ERR_USAGE           = 6,  /**< Неверное число аргументов командной строки. */
    BUILD_ERR_READ_FILE       = 7,  /**< Ошибка чтения файла корпуса (не критична для отдельных файлов). */
    BUILD_ERR_INTERNAL        = 9   /**< Внутренняя ошибка (заглушка). */
} build_status_e;

/**
 * @brief Построить биграммную модель Маркова по корпусу .md файлов.
 *
 * Выполняет два прохода по всем .md файлам в заданной папке:
 * 1. Сбор всех уникальных слов и формирование словаря.
 * 2. Подсчёт частот переходов между словами (биграмм).
 *
 * Полученная модель сохраняется в бинарном (model.bin) и текстовом
 * (vocab.txt) форматах в указанной выходной директории.
 *
 * @param input_dir  Путь к папке с исходными .md файлами.
 * @param output_dir Путь к папке, куда будут записаны model.bin и vocab.txt.
 * @return Код ошибки из enum build_status_e (0 = успех).
 */
int build_model(const char *input_dir, const char *output_dir);

#endif /* MODEL_BUILDER_H */